class Solution{
    public:
    bool isMaxHeap(int a[], int n)
    {
        for(int parent=0; parent<=(n-1)/2; parent++){
            int left = 2*parent+1;
            int right = 2*parent+2;
            if(a[parent]<a[left] || a[parent]<a[right])
                return false;
        }
        return true;
    }
};

